# Find objects in lists

groceries = ['ham','eggs','cheese']
flowers = ['rose','violet','daisy']
primes = [2,3,5,7,11,13]

print(groceries[1])

print(primes[2:4])

print(flowers[1:])

print(primes[:3])

# Add and remove items in lists

flowers.append('buttercup')
print(flowers)

groceries.remove('ham')
print(groceries)
